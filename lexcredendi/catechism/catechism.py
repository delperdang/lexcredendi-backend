    def _insert_asterisk(self, text):
        """Inserts a "*" between each character in a string."""
        new_text = ""
        for char in text:
            new_text += char + "*"
        return new_text[:-1]

    def linkify(self, record_string):
        """Replaces substrings in a record string based on matching and response lists."""
        if not record_string:
            return ""

        temp_record_string = record_string
        for key, value in self.CATECHISM_PARAGRAPHS.items():

            if key in temp_record_string.upper():
                key_w_asterisks = self._insert_asterisk(key)

                temp_record_string = temp_record_string.replace(
                    key, f"__TEMP_REPLACE_{key_w_asterisks}__"
                )

        for key, value in self.CATECHISM_PARAGRAPHS.items():
            key_w_asterisks = self._insert_asterisk(key)
            placeholder = f"__TEMP_REPLACE_{key_w_asterisks}__"
            if placeholder in temp_record_string:
                link_html = '<a href="{}">{}</a>'.format(value, key)
                temp_record_string = temp_record_string.replace(placeholder, link_html)

        return temp_record_string
